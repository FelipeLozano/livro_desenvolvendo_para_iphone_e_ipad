#import "Carro.h"

@implementation Carro

@synthesize _id, tipo;
@synthesize nome, desc, url_foto, url_info, url_video;
@synthesize latitude, longitude;

- (void) dealloc {
    [tipo release];

	[nome release];
    [desc release];
    [url_foto release];
    [url_info release];
    [url_video release];
    [latitude release];
    [longitude release];

    [super dealloc];
}
@end